const $ = (selector, scope = document) => scope.querySelector(selector);
const $$ = (selector, scope = document) => Array.from(scope.querySelectorAll(selector));

const slides = $$('.slide');
const dotsContainer = $('#sliderDots');
let currentSlide = 0;
let sliderInterval;

function buildDots() {
  slides.forEach((_, index) => {
    const button = document.createElement('button');
    button.type = 'button';
    button.setAttribute('aria-label', `Slide ${index + 1}`);
    if (index === 0) button.classList.add('is-active');
    button.addEventListener('click', () => {
      goToSlide(index);
      restartSlider();
    });
    dotsContainer.appendChild(button);
  });
}

function goToSlide(index) {
  slides[currentSlide]?.classList.remove('is-active');
  dotsContainer.children[currentSlide]?.classList.remove('is-active');
  currentSlide = index;
  slides[currentSlide]?.classList.add('is-active');
  dotsContainer.children[currentSlide]?.classList.add('is-active');
}

function nextSlide() {
  goToSlide((currentSlide + 1) % slides.length);
}

function startSlider() {
  sliderInterval = window.setInterval(nextSlide, 5000);
}

function restartSlider() {
  window.clearInterval(sliderInterval);
  startSlider();
}

const sections = $$('main .section');
const navLinks = $$('.nav a[data-nav]');
const observer = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (!entry.isIntersecting) return;
    const id = entry.target.id;
    navLinks.forEach((link) => {
      link.classList.toggle('is-active', link.getAttribute('href') === `#${id}`);
    });
  });
}, {
  threshold: 0.55
});

sections.forEach((section) => observer.observe(section));

const navToggle = $('#navToggle');
const nav = $('#siteNav');
navToggle?.addEventListener('click', () => {
  const isOpen = nav.classList.toggle('is-open');
  document.body.classList.toggle('menu-open', isOpen && window.innerWidth <= 920);
  navToggle.setAttribute('aria-expanded', String(isOpen));
});

navLinks.forEach((link) => {
  link.addEventListener('click', () => {
    nav.classList.remove('is-open');
    document.body.classList.remove('menu-open');
    navToggle?.setAttribute('aria-expanded', 'false');
  });
});

function getValue(obj, path) {
  return path.split('.').reduce((acc, key) => acc?.[key], obj);
}

function applyTranslations(lang) {
  const dictionary = translations[lang] || translations.es;
  document.documentElement.lang = lang;
  document.title = dictionary.pageTitle;

  $$('[data-i18n]').forEach((element) => {
    const key = element.dataset.i18n;
    const value = getValue(dictionary, key);
    if (typeof value === 'string') {
      element.textContent = value;
    }
  });

  localStorage.setItem('criosho-lang', lang);
}

const languageSelect = $('#languageSelect');
const savedLanguage = localStorage.getItem('criosho-lang') || 'es';
languageSelect.value = savedLanguage;
applyTranslations(savedLanguage);

languageSelect.addEventListener('change', (event) => {
  applyTranslations(event.target.value);
});

buildDots();
startSlider();

window.addEventListener('resize', () => {
  if (window.innerWidth > 920) {
    nav.classList.remove('is-open');
    document.body.classList.remove('menu-open');
    navToggle?.setAttribute('aria-expanded', 'false');
  }
});
